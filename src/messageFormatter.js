function escapeMarkdown(text) {
  if (!text) return '';
  return text
    .replace(/_/g, '\\_')
    .replace(/\*/g, '\\*')
    .replace(/\[/g, '\\[')
    .replace(/\]/g, '\\]')
    .replace(/`/g, '\\`');
}

function formatGitsaweMessage(gitsawe, ethDate) {
  if (!gitsawe) return null;
  
  let message = `­ЪЊќ *рїЇрїйрІї - рІерІЋрѕѕрЅ▒ ріЋрЅБрЅЦ*\n`;
  message += `­ЪЊЁ ${formatEthiopianDate(ethDate)}\n`;
  message += `РћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂРћЂ\n\n`;
  
  if (gitsawe.title) {
    message += `­ЪїЪ *${escapeMarkdown(gitsawe.title)}*\n`;
    message += `РћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕРћѕ\n\n`;
  }
  
  if (gitsawe.content && Array.isArray(gitsawe.content)) {
    for (const [index, section] of gitsawe.content.entries()) {
      if (section.title) {
        const prefix = section.title.match(/^[­ЪЊїРюЮ№ИЈ­ЪЊќ­ЪћЇРГљ]/u) ? '' : '­ЪЊї ';
        message += `${prefix}*${escapeMarkdown(section.title)}*\n`;
        message += `РќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈРќФ№ИЈ\n`;
      }
      if (section.main) {
        const truncatedMain = section.main.length > 1000 
          ? section.main.substring(0, 1000) + '...\n\n_­ЪЊќ рЅ░рїерѕЏрѕф ріЋрЅБрЅЦ ріарѕѕ..._' 
          : section.main;
        message += `${escapeMarkdown(truncatedMain)}\n\n`;
      }
      
      // Add separator between sections
      if (index < gitsawe.content.length - 1) {
        message += `РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»РІ»\n\n`;
      }
    }
  }
  
  return message;
}

